#include <stdio.h>
#include <math.h>


int main()
{
    printf("PI = %lf\n", M_PI);
    printf("cos(pi) = %lf\n", cos(M_PI));
    printf("e = %lf\n", M_E);
    printf("exp(1) = %lf\n", exp(1));
    printf("log(e) = %lf\n", log(M_E));
    printf("log10(100) = %lf\n", log10(100));
    printf("ceil(1.3) = %lf\n", ceil(1.3));
    printf("floor(1.3) = %lf\n", floor(1.3));
    printf("round(1.3) = %lf\n", round(1.3));

    return 0;
}
